#include <iostream>
using namespace std;


int main9()
{
	//转义字符 一般用反斜杠\开头

	//换行符 \n
	cout << "hello C++\n" << endl;
	//反斜杠 \\ 
	cout << "hello C++\\" << endl;
	//水平制表符 \t (用于整齐输出数据)
	cout << "aaa\thello C++" << endl;
	cout << "aa\thello C++" << endl;
	cout << "aaaaaaa\thello C++" << endl;
	cout << "aaaaaaaa\thello C++" << endl;


	system("pause");

	return 0;
}